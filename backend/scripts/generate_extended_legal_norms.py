def unique_list(items: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for item in items:
        norm = item.strip()
        if norm and norm not in seen:
            seen.add(norm)
            result.append(norm)
    return result


def make_norm(
    idx: int,
    contract_type: str,
    profile: dict,
    template: dict,
) -> dict:
    safe_norm = template["safe_norm"].format(**profile)
    risky_pattern = template["risky_pattern"].format(**profile)
    deception_patterns = [
        item.format(**profile) for item in template["deception_patterns"]
    ]
    legal_basis = unique_list(
        template.get("legal_basis", []) + profile.get("law_basis", []) + COMMON_LAW_BASIS
    )[:8]

    return {
        "id": idx,
        "contract_type": contract_type,
        "risk_category": template["risk_category"],
        "topic": template["topic"],
        "safe_norm": safe_norm,
        "risky_pattern": risky_pattern,
        "criticality": template["criticality"],
        "deception_patterns": deception_patterns,
        "legal_basis": legal_basis,
        "explanation": "Норма направлена на снижение юридических и финансовых рисков по российскому праву.",
    }


def build_dataset() -> list[dict]:
    norms: list[dict] = []
    idx = 1

    # Universal norms
    for tmpl in UNIVERSAL_TEMPLATES:
        norms.append(
            make_norm(
                idx=idx,
                contract_type="все",
                profile={"law_basis": []},
                template=tmpl,
            )
        )
        idx += 1

    # Digital contracts
    for contract_type, profile in DIGITAL_PROFILES.items():
        for tmpl in DIGITAL_TEMPLATES:
            norms.append(make_norm(idx=idx, contract_type=contract_type, profile=profile, template=tmpl))
            idx += 1

    # Transaction contracts
    for contract_type, profile in TRANSACTION_PROFILES.items():
        for tmpl in TRANSACTION_TEMPLATES:
            norms.append(make_norm(idx=idx, contract_type=contract_type, profile=profile, template=tmpl))
            idx += 1

    # NDA
    for tmpl in NDA_TEMPLATES:
        norms.append(make_norm(idx=idx, contract_type=NDA_PROFILE["contract_type"], profile=NDA_PROFILE, template=tmpl))
        idx += 1

    # Labor
    for tmpl in LABOR_TEMPLATES:
        norms.append(make_norm(idx=idx, contract_type=LABOR_PROFILE["contract_type"], profile=LABOR_PROFILE, template=tmpl))
        idx += 1

    return norms


def main() -> None:
    data_path = Path(__file__).resolve().parent.parent / "data" / "legal_norms.json"
    norms = build_dataset()
    data_path.write_text(json.dumps(norms, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Generated {len(norms)} norms -> {data_path}")


if __name__ == "__main__":
    main()

